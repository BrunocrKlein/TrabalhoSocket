	public void customerHand(String[][] cards) {
		for (int i = 0; i < cards.length; i++) {
			img = new ImageIcon(getClass().getResource(cards[i][1]));
			img.setImage(img.getImage().getScaledInstance(labels[i].getWidth(), labels[i].getHeight(), 100));
			labels[i].setIcon(img);
		}
	}

